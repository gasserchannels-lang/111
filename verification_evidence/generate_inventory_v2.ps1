function ClassifyFile($filePath) {
    if ($filePath -like "tests/*.php" -or $filePath -like "testsprite_tests/*.py" -or $filePath -like "*.spec.js") {
        return "test"
    }
    if ($filePath -like "config/*.php" -or $filePath -like ".env*" -or $filePath -in @("composer.json", "package.json", "docker-compose.yml", ".gitlab-ci.yml", "phpunit.xml", "phpstan.neon")) {
        return "config"
    }
    if ($filePath -like "docs/*" -or $filePath -in @("README.md", "CHANGELOG.md", "CONTRIBUTING.md")) {
        return "doc"
    }
    if ($filePath -like "public/*" -or $filePath -like "resources/css/*" -or $filePath -like "resources/js/*") {
        if (!($filePath -like "*.php" -or $filePath -like "*.js")) {
            return "asset"
        }
    }
    if ($filePath -like "*.php" -or $filePath -like "*.js" -or $filePath -like "*.ts" -or $filePath -like "*.py") {
        return "source"
    }
    return "other"
}

