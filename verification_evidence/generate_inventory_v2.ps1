        $testFileCounts[$ext]++
    }
}

$summaryContent = "Total files: $($fileList.Count)`n"
$summaryContent += "Total size (bytes): $($totalSize)`n"
$summaryContent += "Test files by extension/pattern:`n"
foreach ($entry in $testFileCounts.GetEnumerator()) {
    $summaryContent += "  $($entry.Name): $($entry.Value)`n"
}

# Output the CSV and summary content, separated by a marker
Write-Output "---CSV_START---"
Write-Output $csvContent
Write-Output "---CSV_END---"
Write-Output "---SUMMARY_START---"
Write-Output $summaryContent
Write-Output "---SUMMARY_END---"
